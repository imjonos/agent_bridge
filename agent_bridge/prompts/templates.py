from __future__ import annotations


def build_builder_prompt(task: str) -> str:
    return f"""Ты основной агент-разработчик.

Задача:
{task}

Работай в проекте. Вноси изменения аккуратно.
После выполнения кратко напиши:
1. Что изменено.
2. Какие файлы затронуты.
3. Как проверить результат.
4. Есть ли риски или незавершённые места.
"""


def build_reviewer_prompt(task: str, builder_output: str, git_status: str, git_diff: str) -> str:
    return f"""Ты агент-ревьюер.

Проверь результат работы основного агента.

Исходная задача:
{task}

Ответ основного агента:
{builder_output}

Git status:
{git_status}

Git diff:
{git_diff}

Твоя задача:
1. Проверить только блокирующие проблемы: нарушение исходной задачи, баги, поломки существующего поведения, опасные изменения, ошибки безопасности или данных.
2. Проверить, что добавленные/изменённые тесты достаточны для затронутого поведения. Требуй новые тесты только когда без них нельзя надёжно проверить рискованное изменение.
3. Не блокировать результат из-за вкусовых предпочтений, косметики, альтернативного дизайна кода, микрооптимизаций или улучшений вне исходной задачи.
4. Не требовать расширения scope, рефакторинга или идеальной архитектуры, если текущая реализация корректно решает задачу и не создаёт существенного риска.
5. Если замечание не является обязательным для корректности задачи, не отправляй его builder'у.
6. Для каждой блокирующей проблемы дай конкретную правку и объясни, какой риск она устраняет.

Если блокирующих проблем нет, ответь строго:
OK

Если есть блокирующие проблемы, верни только их список и конкретные инструкции для исправления.
"""


def build_fix_prompt(task: str, review_output: str, git_status: str, git_diff: str) -> str:
    return f"""Ты основной агент-разработчик.

Ревьюер проверил твою работу и оставил замечания.

Исходная задача:
{task}

Замечания ревьюера:
{review_output}

Текущий git status:
{git_status}

Текущий git diff:
{git_diff}

Исправь замечания.
После исправления кратко напиши:
1. Что исправлено.
2. Какие файлы изменены.
3. Как проверить результат.
4. Остались ли спорные моменты.
"""
